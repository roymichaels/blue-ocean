// scripts/fix-waku-path.js
const fs = require('fs');
const path = require('path');

function log(...a){ console.log('[fix-waku]', ...a); }
function exists(p){ try { return fs.existsSync(p); } catch { return false; } }

function getCoreRoot() {
  try {
    const corePkg = require.resolve('@waku/core/package.json');
    return path.dirname(corePkg);
  } catch {
    const fallback = path.join(
      process.cwd(),
      'node_modules',
      '@waku',
      'core'
    );
    return exists(fallback) ? fallback : null;
  }
}

function findActualVersionFile(coreRoot) {
  const candidates = [
    'dist/lib/message/version_0.js',
    'dist/lib/message/version-0.js',
    'lib/message/version_0.js',
    'lib/message/version-0.js',
    'dist/lib/message/version_0/index.js',
    'lib/message/version_0/index.js',
  ];
  for (const rel of candidates) {
    const abs = path.join(coreRoot, rel);
    if (exists(abs)) return { rel, abs };
  }
  return null;
}

function patchSdkImport(sdkRoot, fromList, toId) {
  const candidates = [
    'dist/index.js','dist/index.cjs','index.js','dist/src/index.js'
  ].map(f => path.join(sdkRoot, f)).filter(exists);

  let changed = 0;
  for (const file of candidates) {
    const src = fs.readFileSync(file, 'utf8');
    let out = src;
    for (const from of fromList) {
      out = out.replaceAll(from, toId);
    }
    if (out !== src) {
      fs.writeFileSync(file, out, 'utf8');
      log('Patched', path.relative(process.cwd(), file), '->', toId);
      changed++;
    }
  }
  if (!changed) log('No @waku/sdk file needed patching.');
}

function ensureShim(coreRoot, actualRel) {
  const shimRel = 'lib/message/version_0.js';
  const shimAbs = path.join(coreRoot, shimRel);
  const dir = path.dirname(shimAbs);
  fs.mkdirSync(dir, { recursive: true });

  const targetAbs = path.join(coreRoot, actualRel);
  const relFromShim = path.relative(dir, targetAbs).split('\\').join('/');

  const shim =
`// Auto-generated by fix-waku-path.js
export * from '${relFromShim}';
export { default } from '${relFromShim}';
`;
  fs.writeFileSync(shimAbs, shim, 'utf8');
  log('Created shim', shimRel, '->', actualRel);
}

function ensureExport(coreRoot, actualRel) {
  const pkgPath = path.join(coreRoot, 'package.json');
  let pkg;
  try {
    pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
  } catch {
    return;
  }
  pkg.exports = pkg.exports || {};
  const rel = './' + actualRel.split('\\').join('/');
  const entry = pkg.exports['./lib/message/version_0'] || {};
  if (!entry.import) entry.import = rel;
  if (!entry.require) entry.require = rel;
  if (!entry.default) entry.default = rel;
  pkg.exports['./lib/message/version_0'] = entry;
  fs.writeFileSync(pkgPath, JSON.stringify(pkg, null, 2));
  log('Ensured export ./lib/message/version_0 ->', rel);
}

(function main(){
  const coreRoot = getCoreRoot();
  if (!coreRoot) {
    console.error('[fix-waku] @waku/core not found. Did you install deps?');
    process.exit(1);
  }

  const actual = findActualVersionFile(coreRoot);
  if (!actual) {
    console.error('[fix-waku] Could not find version_0 in @waku/core. Please ensure @waku/core is installed correctly.');
    process.exit(1);
  }
  log('Detected @waku/core message path:', actual.rel);

  const moduleId = `@waku/core/${actual.rel.split('\\').join('/')}`;

  let sdkRoot;
  try {
    sdkRoot = path.dirname(require.resolve('@waku/sdk/package.json'));
  } catch {
    const fallback = path.join(
      process.cwd(),
      'node_modules',
      '@waku',
      'sdk'
    );
    if (!exists(fallback)) {
      console.error('[fix-waku] @waku/sdk not found.');
      process.exit(1);
    }
    sdkRoot = fallback;
  }

  const badIds = [
    '@waku/core/lib/message/version_0',
    '@waku/core/lib/message/version-0',
    '@waku/core/dist/message/version_0',
    '@waku/core/dist/message/version-0'
  ];
  patchSdkImport(sdkRoot, badIds, moduleId);
  ensureShim(coreRoot, actual.rel);
  ensureExport(coreRoot, actual.rel);

  log('Done.');
})();
